marks = 85

# Conditions check karne ke liye if-elif-else statement ka use kiya
if marks == 100:
    print("Topper")
elif marks > 90:
    print("A+ Grade")
elif marks > 80:
    print("A Grade")
elif marks > 70:
    print("B Grade")
elif marks > 40:
    print("Pass")
else:
    print("Fail")  # Agar marks 40 ya usse kam hain